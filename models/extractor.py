class Model(nn.Module):
    def __init__(self,height=720,width=1280,stride=16,num_feature_levels=3,num_channels=[96,192,384],hidden_dim=128) -> None:
        super().__init__()
        self.backbone = build_backbone()
        self.transformer=Transformer(d_model=hidden_dim,nhead=8,num_encoder_layers=6,num_decoder_layers=6,dim_feedforward=2048,dropout=0.1,normalize_before=False,return_intermediate_dec=False)
        self.ot_loss = ot_similarity()

        input_proj_list=[]
        for i in range(num_feature_levels):
            input_proj_list.append(nn.Sequential(
                nn.Conv2d(num_channels[i],hidden_dim,kernel_size=1,stride=1,padding=0),
                nn.GroupNorm(32,hidden_dim),
                nn.GELU(),
            ))
            for j in range(num_feature_levels-1-i):
                input_proj_list[i]+=nn.Sequential(
                    nn.Conv2d(hidden_dim,hidden_dim,kernel_size=3,stride=2,padding=1),
                    nn.GroupNorm(32,hidden_dim),
                    nn.GELU(),
                )
        self.input_fuse=nn.Sequential(
            nn.Conv2d(hidden_dim*num_feature_levels,hidden_dim,kernel_size=1,stride=1,padding=0),
            nn.GroupNorm(32,hidden_dim),
            nn.GELU(),
        )
        self.input_proj=nn.ModuleList(input_proj_list)
        for proj in self.input_proj:
            nn.init.xavier_uniform_(proj[0].weight, gain=nn.init.calculate_gain('relu'))
            nn.init.constant_(proj[0].bias, 0)
        self.hidden_dim=hidden_dim
        self.pos_embedder=PositionEmbeddingSine(hidden_dim//2,normalize=True)
    def forward(self, x, ref_points):
        x1=x[:,0:3,:,:]
        x2=x[:,3:6,:,:]
        ref_point1=ref_points[:,0,...]
        ref_point2=ref_points[:,1,...]

        z1 = self.backbone(x1)
        z2 = self.backbone(x2)
        z1_list=[]
        z2_list=[]
        for i in range(len(z1)):
            z1_list.append(self.input_proj[i](z1[i]))
            z2_list.append(self.input_proj[i](z2[i]))
        z1=torch.cat(z1_list,dim=1)
        z1=self.input_fuse(z1)
        z2=torch.cat(z2_list,dim=1)
        z2=self.input_fuse(z2)
        query_embed1 = pos2posemb2d(ref_point1, num_pos_feats=self.hidden_dim//2).float()
        query_embed2 = pos2posemb2d(ref_point2, num_pos_feats=self.hidden_dim//2).float()
        pos_emd_1=self.pos_embedder(z1_list[0].permute(0,2,3,1))
        pos_emd_2=self.pos_embedder(z2_list[0].permute(0,2,3,1))
        mask1=mask2=torch.ones((z1.shape[0],z1.shape[2],z1.shape[3])).cuda().bool()
        print(z1.shape,pos_emd_1.shape,query_embed1.shape)
        features1=self.transformer(z1,mask1,query_embed1,pos_emd_1)
        features2=self.transformer(z2,mask2,query_embed2,pos_emd_2)
        features1=F.relu(features1)
        features2=F.relu(features2)
        return features1,features2
    def loss(self,features1,features2):
        loss=self.ot_loss(features1,features2)
        return loss
def build_model():
    model=Model()
    return model
        
